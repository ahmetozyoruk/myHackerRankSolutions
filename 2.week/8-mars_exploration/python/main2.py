def marsExploration(s):
    sos, res = "SOS", 0
    for i, c in enumerate(s):
        if c != sos[i%3]:
            res += 1
    return res

if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    s = input()

    result = marsExploration(s)

    fptr.write(str(result) + '\n')

    fptr.close()

